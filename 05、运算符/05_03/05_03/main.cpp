#include <iostream>
using namespace std;

/*

==     等于
!=     不等于
<      小于
>      小于
<=     小于等于
>=     大于等于

*/

int main() {
	int a = 6;
	int b = 9;

	cout << (a == b) << endl;
	cout << (a != b) << endl;
	cout << (a > b) << endl;
	cout << (a < b) << endl;
	cout << (a >= b) << endl;
	cout << (a <= b) << endl;

	return 0;
}